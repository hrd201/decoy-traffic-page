# 每日进化报告生成器

## 任务：每日 23:00 进化报告

### 执行步骤

1. **回顾今日会话**
   - 读取今日所有会话历史
   - 提取关键决策和事件

2. **提取要点**
   - 新学会的知识
   - 遇到的问题
   - 解决方案

3. **分析进化**
   - 认知提升
   - 错误反思
   - 技能固化建议

4. **生成报告**
   - 压缩为简洁的每日总结
   - 提议 3 个可固化的技能点

---

## 报告模板

```markdown
# 📈 每日进化报告 - YYYY-MM-DD

## 今日要点
- [关键事件1]
- [关键事件2]
- [关键事件3]

## 🧠 认知提升
- 学会的新东西1
- 学会的新东西2

## 🔧 问题与解决
| 问题 | 解决方案 |
|------|----------|
| 问题1 | 方案1 |

## ⭐ 技能固化建议
1. [技能1]
2. [技能2]
3. [技能3]

## 明日待办
- [ ]
```

---

## 执行脚本

此任务通过 OpenClaw cron 自动执行，每天 23:00 发送报告。
